        <h1>Вспенивание молока, вливание и латте-арт</h1>        
Если приготовление эспрессо можно в значительной степени доверить машине 
с автоматическим проливом воды, то приготовлению молочных напитков следует уделить больше внимания.
Эспрессо с молоком и молочной пеной – отличное сочетание.
 Однако, чтобы научиться вспенивать и вливать молоко в кофе, 
 потребуется немало усилий и терпения. Вы должны добиться нужной глянцевой текстуры 
 и не довести молоко до кипения. На результат влияет все – от положения стимера
  до скорости вливания. Латте-арт является обязательным навыком бариста.
За определением самого подходящего молока стоит целая наука. 
Известно, что альтернативные виды молока сложнее вспенивать 
и использовать для рисунков на латте. В большинстве кофеен латте-арт 
– непременное условие подачи напитка. Набравшись опыта, вы сможете экспериментировать
 с самыми разными рисунками – насколько хватит вашего воображения.
В этой области навыки бариста не ограничиваются только умением правильного вспенивания молока. 
Важны знания основных компонентов (белки, жиры, углеводы) и методов обработки молока. 
