        <h1>Помол</h1>
        
Правильный выбранный помол – секрет вкусного эспрессо. 
Зерно нужно молоть строго перед приготовлением,
 потому что в кофейных зёрнах быстро улетучиваются вкусовые вещества.
  Вы должны понимать, как степень помола влияет на экстракцию 
  и что именно подходит для определенного метода приготовления кофе. 
  Чем мельче помол, тем быстрее экстракция и тем больше площадь поверхности 
  молотого зерна, а значит – тем медленнее через него проходит вода.
Если ваш кофе горчит, используйте зерно более крупной фракции: так замедлите 
экстракцию и укоротите время заваривания.
Если кофе слишком кислый, попробуйте изменить помол на более мелкий.
Кофемолки страдают из-за накопления остатков кофейных масел и частиц,
 которые собираются вокруг жерновов и вдоль дозирующих трубок или выпускных отверстий.
  Эти остатки добавляют затхлый привкус кофе и затрудняют свободное 
  и точное дозирование помола. По этой причине бариста обязан следить 
  и своевременно чистить кофемолку. 
